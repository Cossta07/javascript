/*

Como você pode converter um número em uma string em 
JavaScript?
Resposta:
O método toString() é uma forma comum e simples de converter um número em uma string em JavaScript. 
Podemos aplicar esse método diretamente ao número que queremos converter, usando a sintaxe “numero.toString()”
*/
let numero = 123;
let stringNumero = numero.toString();
console.log(stringNumero); // Saída: "123"