/*

Como você pode converter uma string em um número inteiro em 
JavaScript?
Resposta:
Utilizando a Função parseInt() para Converter uma String em um Número Inteiro em JavaScript.
 A função parseInt() é amplamente utilizada em JavaScript para converter uma string em um número inteiro.
*/
let numeroString = "42";
let numero = parseInt(numeroString);

console.log(numero); // 42