/*
Como você pode obter o comprimento de uma string em 
JavaScript?
Resposta:
Uma das maneiras mais simples de obter o comprimento de uma string em JavaScript é utilizando a propriedade length. 
Essa propriedade retorna o número de caracteres da string, incluindo espaços em branco e caracteres especiais.
*/
let minhaString = "Olá, mundo!";

let comprimento = minhaString.length;
 
 console.log(comprimento); // Output: 12